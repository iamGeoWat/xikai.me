function framePattern(grid: string[], size: number, color: string): string {
  const dots: string[] = []
  for (let y = 0; y < grid.length; y++) {
    for (let x = 0; x < grid[y].length; x++) {
      if (grid[y][x] === '#') {
        dots.push(`${x * size}px ${y * size}px 0 0 ${color}`)
      }
    }
  }
  return dots.join(',')
}

interface PixelSpriteProps {
  name?: keyof typeof PIXEL_SPRITES
  size?: number
  color?: string
  fps?: number
  className?: string
}

export function PixelSprite({
  name = 'buddy',
  size = 4,
  color = '#1c1a15',
  fps = 3,
  className = '',
}: PixelSpriteProps) {
  const frames = PIXEL_SPRITES[name] || PIXEL_SPRITES.buddy
  const [frame, setFrame] = useState(0)

  useEffect(() => {
    if (frames.length < 2) return
    const id = setInterval(() => {
      setFrame((f) => (f + 1) % frames.length)
    }, 1000 / fps)
    return () => clearInterval(id)
  }, [frames.length, fps])

  const grid = frames[frame]
  const w = grid[0].length * size
  const h = grid.length * size

  return (
    <span
      className={`inline-block relative ${className}`}
      style={{ width: w, height: h }}
      aria-hidden="true"
    >
      <span
        className="absolute top-0 left-0"
        style={{
          width: size,
          height: size,
          boxShadow: framePattern(grid, size, color),
          imageRendering: 'pixelated',
        }}
      />
    </span>
  )
}
